public class Event {

    private String Date;
    private String Day;
    private String EventNo;
    private String Location;
    private String Time;
    private String Title;

    public String getDate () {
        return Date;
    }

    public String getDay () {
        return Day;
    }

    public String getLocation () {
        return Location;
    }

    public String getEventNo () {
        return EventNo;
    }

    public String getTime () {
        return Time;
    }

    public String getTitle () {
        return Title;
    }



}
